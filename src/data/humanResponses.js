export const getRandomResponse = (category) => {
  const responses = HUMAN_RESPONSES[category];
  if (!responses || responses.length === 0) {
    return null;
  }
  return responses[Math.floor(Math.random() * responses.length)];
};

/**
 * Obtiene mÃºltiples respuestas aleatorias sin repetir
 */
export const getRandomResponses = (category, count = 3) => {
  const responses = HUMAN_RESPONSES[category];
  if (!responses || responses.length === 0) {
    return [];
  }

  const shuffled = [...responses].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, responses.length));
};

